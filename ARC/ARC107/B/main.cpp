ull gcd(ull a, ull b) { return b ? gcd(b, a % b) : a; }
ull lcm(ull a, ull b) { return a / gcd(a, b) * b; }

int main(){
    ll n, k;
    cin >> n >> k;
    ll ans = 0;
    ll l = max(2LL, k+2), r = min(2*n, k+2*n);
    for (ll val=l; val<=r; ++val) {
        ll val2 = val - k;
        if (val2<2 || val2>2*n) continue;
        ll l1 = max(1LL, val-n), r1 = min(n, val-1), ab = max(0LL, r1-l1+1);
        ll l2 = max(1LL, val2-n), r2 = min(n, val2-1), cd = max(0LL, r2-l2+1);
        ans += ab * cd;
    }
    cout << ans << endl;
    return 0;
}
